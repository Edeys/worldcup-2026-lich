function toVnTime(utcStr) {
  const d = new Date(utcStr + "Z");
  return new Date(Date.UTC(
    d.getUTCFullYear(), d.getUTCMonth(), d.getUTCDate(),
    d.getUTCHours() + 7, d.getUTCMinutes()
  ));
}
function formatDateVN(date) {
  const days = ["CN", "T2", "T3", "T4", "T5", "T6", "T7"];
  return `${days[date.getUTCDay()]}, ${String(date.getUTCDate()).padStart(2, "0")}/${String(date.getUTCMonth() + 1).padStart(2, "0")}`;
}
function formatTimeVN(date) {
  return `${String(date.getUTCHours()).padStart(2, "0")}:${String(date.getUTCMinutes()).padStart(2, "0")}`;
}
function getKhungGio(date) {
  const h = date.getUTCHours();
  if (h < 12) return { label: "Sang" };
  if (h < 18) return { label: "Chieu" };
  return { label: "Toi" };
}

